# Arithmetic Operators
print(5+7-6)  # addition and subtraction
print('Elon'+' Musk')
print(9/6)  # division
print(9//6)  # floor division
print(8*8)  # multiplication
print('Thank you'*404)
print(8**3)  # exponent
print(8 % 3)  # modulus

# Comparison Operators
# >, <, <=, >=, ==, !=
marks = 52
print(marks >= 32)
print(marks < 32)
print(marks == 32)
print(marks != 0)


# Logical Operations
# and, or, not
print(marks > 32 and marks < 80)
print(not marks == 0)

# Eligible to escape from titanic
gender = 'female'
age = 20
print(age < 16 or gender == 'female')
